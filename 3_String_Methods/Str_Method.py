name = "saif"

#prints length of string
print(len(name))

#finds index in string
print(name.find("S"))

#capitalizes first letter in string
print(name.capitalize())

#Makes string all upper case
print(name.upper())

#makes string all lower case
print(name.lower())

#returns true or false if string is digit
print(name.isdigit())

#checks to see if string is alphabetical characters
print(name.isalpha())

#counts how many characters in the string
print(name.count("i"))

#replace characters in a string
print(name.replace("f", "!"))

#prints string three time
print(name * 3)
